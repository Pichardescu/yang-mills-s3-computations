def conjugacy_classes_cyclic(n):
    """
    Conjugacy classes of Z_n in SU(2).

    Z_n is generated by g = diag(e^{2*pi*i/n}, e^{-2*pi*i/n}),
    which has SU(2) rotation angle theta = 4*pi/n.
    The s-th power g^s has angle theta_s = 4*pi*s/n.

    g^n = I (not -I), so this is genuinely order n.
    Z_n is abelian, so each element is its own conjugacy class.

    The quotient S^3/Z_n is the lens space L(n, 1).

    Returns
    -------
    list of (size, theta) : conjugacy classes
    int : group order
    """
    classes = []
    for s in range(n):
        theta = 4 * np.pi * s / n
        classes.append((1, theta))
    return classes, n


def conjugacy_classes_binary_dihedral(n):
    """
    Conjugacy classes of the binary dihedral group D*_n (order 4n).

    D*_n < SU(2) is generated by:
      a = diag(e^{i*pi/n}, e^{-i*pi/n})  (order 2n in SU(2), theta = 2*pi/n)
      b = [[0, i], [i, 0]]               (order 4, theta = pi, b^2 = -I = a^n)

    Elements: {a^s : s = 0..2n-1} union {a^s*b : s = 0..2n-1}, total 4n.

    The SU(2) rotation angles are:
      a^s has eigenvalues e^{i*s*pi/n}, e^{-i*s*pi/n}, hence theta = 2*pi*s/n.
      a^s*b has trace 0 (since b is off-diagonal), hence theta = pi.

    Conjugacy classes:
      1. {e}:                             1 element, theta = 0
      2. {a^n} = {-I}:                    1 element, theta = 2*pi
      3. {a^s, a^{2n-s}} for s=1..n-1:   n-1 classes of size 2, theta = 2*pi*s/n
      4. n even: two reflection classes of size n, both at theta = pi
         n odd:  one reflection class of size 2n, at theta = pi

    Parameters
    ----------
    n : int >= 2
        The index in D*_n. Group order is 4n.

    Returns
    -------
    list of (size, theta) : conjugacy classes
    int : group order (4n)
    """
    classes = []

    # Identity
    classes.append((1, 0.0))
    # Central element -I (= a^n, theta = 2*pi*n/n = 2*pi)
    classes.append((1, 2 * np.pi))

    # Rotation classes: {a^s, a^{2n-s}} for s = 1, ..., n-1
    # a^s has theta = 2*pi*s/n
    for s in range(1, n):
        theta = 2 * np.pi * s / n
        classes.append((2, theta))

    # Reflection classes: all elements a^s * b have trace 0, theta = pi
    # n even: two classes of size n
    # n odd: one class of size 2n
    if n % 2 == 0:
        classes.append((n, np.pi))
        classes.append((n, np.pi))
    else:
        classes.append((2 * n, np.pi))

    order = 4 * n

    # Verify
    total = sum(s for s, _ in classes)
    assert total == order, f"D*_{n}: classes sum to {total}, expected {order}"

    return classes, order


def conjugacy_classes_binary_tetrahedral():
    """
    Conjugacy classes of the binary tetrahedral group T* (order 24).

    T* < SU(2) is the preimage of the tetrahedral rotation group T (order 12).
    As quaternions: 8 from Q_8 (+-1, +-i, +-j, +-k) plus 16 half-integer
    quaternions (+-1/2, +-1/2, +-1/2, +-1/2).

    T* has 7 conjugacy classes, but only 5 distinct trace values.
    For the character formula, only the trace (= rotation angle) matters,
    so we use 5 trace-grouped classes:

      1. {e}:     1 element,  theta = 0     (tr = +2)
      2. {-e}:    1 element,  theta = 2*pi  (tr = -2)
      3. C3:      8 elements, theta = 2*pi/3  (tr = +1)
      4. C3inv:   8 elements, theta = 4*pi/3  (tr = -1)
      5. C2:      6 elements, theta = pi     (tr = 0, the +-i, +-j, +-k)

    Total: 1 + 1 + 8 + 8 + 6 = 24.

    Returns
    -------
    list of (size, theta) : conjugacy classes (grouped by trace)
    int : 24
    """
    classes = [
        (1,  0.0),               # Identity
        (1,  2 * np.pi),         # -Identity
        (8,  2 * np.pi / 3),     # Order 3 rotations (tr = +1)
        (8,  4 * np.pi / 3),     # Inverse rotations (tr = -1)
        (6,  np.pi),             # Order 2 rotations (tr = 0)
    ]
    order = 24
    total = sum(s for s, _ in classes)
    assert total == order, f"T*: classes sum to {total}, expected {order}"
    return classes, order


def conjugacy_classes_binary_octahedral():
    """
    Conjugacy classes of the binary octahedral group O* (order 48).

    O* < SU(2) is the preimage of the octahedral rotation group O (order 24).

    Conjugacy classes (8 classes):
      1. {e}:     1 element,  theta = 0
      2. {-e}:    1 element,  theta = 2*pi
      3. C4_a:    6 elements, theta = pi/2   (order-4 face rotations, lifted)
      4. C4_b:    6 elements, theta = 3*pi/2 (inverses)
      5. C3_a:    8 elements, theta = 2*pi/3 (order-3 vertex rotations, lifted)
      6. C3_b:    8 elements, theta = 4*pi/3 (inverses)
      7. C2:     12 elements, theta = pi     (order-2 edge rotations, lifted)
      8. C6:      6 elements, theta = pi     (from squares of C4, but wait...)

    Actually, more carefully using the character table of O*:
      1. {e}:      1 element, theta = 0
      2. {-e}:     1 element, theta = 2*pi
      3. C8_a:     6 elements, theta = pi/4  (order-8 elements)
      4. C8_b:     6 elements, theta = 7*pi/4 (= 2*pi - pi/4, inverses)
      5. C4:       6 elements, theta = pi/2  (order-4 face rotations)
                   -- wait, this is C8^2, angle pi/2
      6. C6_a:     8 elements, theta = 2*pi/3 (vertex rotations)
      7. C6_b:     8 elements, theta = 4*pi/3 (inverses)
      8. C2:      12 elements, theta = pi     (edge rotations)

    Total: 1+1+6+6+6+8+8+12 = 48. Check.

    Returns
    -------
    list of (size, theta) : conjugacy classes
    int : 48
    """
    classes = [
        (1,  0.0),                  # Identity
        (1,  2 * np.pi),            # -Identity
        (6,  np.pi / 2),            # Order 8 elements (face rotation lifts)
        (6,  3 * np.pi / 2),        # Inverses
        (6,  np.pi),                # Squares of C8 (order 4)
        (8,  2 * np.pi / 3),        # Vertex rotations (order 6)
        (8,  4 * np.pi / 3),        # Inverses
        (12, np.pi),                # Edge rotations (order 4)
    ]
    order = 48
    total = sum(s for s, _ in classes)
    assert total == order, f"O*: classes sum to {total}, expected {order}"
    return classes, order


