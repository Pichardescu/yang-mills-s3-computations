# ===================================================================
# Molien series (trivial multiplicity) for each group type
# ===================================================================

def _multiplicity_from_classes(conjugacy_classes, group_order, k):
    """
    Compute r_Gamma(k) = dim of Gamma-invariant subspace in V_{k/2}.

    r_Gamma(k) = (1/|Gamma|) * sum_g chi_{k/2}(g)
               = (1/|Gamma|) * sum_{classes C} |C| * chi_{k/2}(theta_C)

    Parameters
    ----------
    conjugacy_classes : list of (size, theta)
    group_order : int
    k : int >= 0

    Returns
    -------
    int : multiplicity (guaranteed non-negative integer)
    """
    total = 0.0
    for size, theta in conjugacy_classes:
        chi = character_su2(k, theta)
        total += size * chi
    m = total / group_order
    m_int = int(round(m))
    if m_int < 0:
        # Can happen for very large k due to floating point; should be 0
        if abs(m) < 0.5:
            return 0
        raise ValueError(f"Negative multiplicity: k={k}, raw={m}")
    return m_int


def molien_cyclic(n, k_max):
    """
    Multiplicities r_{Z_n}(k) for k = 0, ..., k_max.

    For cyclic Z_n, the invariant subspace of V_{k/2} (dimension k+1)
    under the cyclic group generated by rotation 2*pi/n.

    Parameters
    ----------
    n : int >= 1
        Order of cyclic group. Z_1 = trivial group (full S^3).
    k_max : int
        Maximum level.

    Returns
    -------
    ndarray of int, shape (k_max+1,)
    """
    classes, order = conjugacy_classes_cyclic(n)
    result = np.zeros(k_max + 1, dtype=int)
    for k in range(k_max + 1):
        result[k] = _multiplicity_from_classes(classes, order, k)
    return result


