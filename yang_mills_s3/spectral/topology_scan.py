def _get_camb_transfer(omega_tot=1.018, l_max=30):
    """
    Run CAMB for closed S^3 and extract transfer data.

    Returns
    -------
    dict with keys:
        'nu_vals': ndarray of int, eigenmode indices
        'delta_sq': dict {l: ndarray}, |Delta_l(nu)|^2 for each l
        'cls_s3': dict {l: D_l}, D_l for simply-connected S^3 in muK^2
    """
    if not HAS_CAMB:
        raise ImportError("CAMB not installed. Run: pip install camb")

    from .cmb_boltzmann import PLANCK_2018

    omega_k = 1.0 - omega_tot
    pars = camb.CAMBparams()
    pars.set_cosmology(
        H0=PLANCK_2018['H0'],
        ombh2=PLANCK_2018['ombh2'],
        omch2=PLANCK_2018['omch2'],
        omk=omega_k,
        tau=PLANCK_2018['tau'],
    )
    pars.InitPower.set_params(
        As=PLANCK_2018['As'],
        ns=PLANCK_2018['ns'],
    )
    pars.set_for_lmax(l_max + 50, lens_potential_accuracy=0)

    results = camb.get_results(pars)

    # Full D_l for S^3
    powers = results.get_cmb_power_spectra(
        params=None, CMB_unit='muK', raw_cl=False
    )
    total = powers['total']
    cls_s3 = {}
    for l in range(2, min(l_max + 1, total.shape[0])):
        cls_s3[l] = total[l, 0]

    # Transfer data
    transfer = results.get_cmb_transfer_data()
    q = transfer.q
    dq = q[1] - q[0]
    nu_vals = np.round(q / dq).astype(int)
    Delta_T = transfer.delta_p_l_k[0]  # Temperature transfer
    L = transfer.L

    delta_sq = {}
    for l_val in L:
        if l_val < 2 or l_val > l_max:
            continue
        l_idx = np.where(L == l_val)[0][0]
        delta_sq[int(l_val)] = Delta_T[l_idx, :] ** 2

    return {
        'nu_vals': nu_vals,
        'delta_sq': delta_sq,
        'cls_s3': cls_s3,
        'L': L,
    }


def compute_suppression(r_gamma, nu_vals, delta_sq):
    """
    Compute suppression ratio S_l for a given topology.

    S_l = [sum_nu T_l(nu)^2 * r_Gamma(k) * nu] / [sum_nu T_l(nu)^2 * nu^2]

    where k = nu - 1.

    Parameters
    ----------
    r_gamma : ndarray of int
        Multiplicities r_Gamma(k) for k = 0, ..., k_max.
    nu_vals : ndarray of int
        CAMB eigenmode indices.
    delta_sq : dict {l: ndarray}
        |Delta_l(nu)|^2 for each multipole l.

    Returns
    -------
    dict {l: S_l}
    """
    k_max = len(r_gamma) - 1
    ratios = {}

    for l, d2 in delta_sq.items():
        # S^3: all modes with weight nu^2
        c_s3 = np.sum(d2 * nu_vals ** 2)

        # S^3/Gamma: surviving modes with weight r_Gamma(k) * nu
        c_gamma = 0.0
        for j, nu in enumerate(nu_vals):
            k = nu - 1
            if 0 <= k <= k_max:
                mk = r_gamma[k]
                if mk > 0:
                    c_gamma += d2[j] * mk * nu

        ratios[l] = c_gamma / c_s3 if c_s3 > 0 else 0.0

    return ratios


def compute_cls_quotient(r_gamma, omega_tot=1.018, l_max=30, camb_data=None):
    """
    Compute C_l for the quotient S^3/Gamma.

    Parameters
    ----------
    r_gamma : ndarray of int
        Multiplicities r_Gamma(k).
    omega_tot : float
        Total density parameter.
    l_max : int
        Maximum multipole.
    camb_data : dict or None
        Pre-computed CAMB transfer data (from _get_camb_transfer).
        If None, CAMB is run.

    Returns
    -------
    dict {l: D_l} in muK^2
    """
    if camb_data is None:
        camb_data = _get_camb_transfer(omega_tot, l_max)

    suppression = compute_suppression(
        r_gamma, camb_data['nu_vals'], camb_data['delta_sq']
    )

    cls_s3 = camb_data['cls_s3']
    cls_gamma = {}

    # Apply suppression where we have CAMB transfer data
    computed_ls = sorted(suppression.keys())
    for l in range(2, l_max + 1):
        d_s3 = cls_s3.get(l, 0.0)
        if l in suppression:
            cls_gamma[l] = d_s3 * suppression[l]
        elif len(computed_ls) >= 2:
            # Interpolate S_l for missing l values
            below = [cl for cl in computed_ls if cl < l]
            above = [cl for cl in computed_ls if cl > l]
            if below and above:
                l_lo, l_hi = below[-1], above[0]
                frac = (l - l_lo) / (l_hi - l_lo)
                s_interp = suppression[l_lo] + frac * (suppression[l_hi] - suppression[l_lo])
            elif below:
                s_interp = suppression[below[-1]]
            elif above:
                s_interp = suppression[above[0]]
            else:
                s_interp = 1.0
            cls_gamma[l] = d_s3 * s_interp
        else:
            cls_gamma[l] = d_s3

    return cls_gamma


def scan_all_ade_groups(omega_tot=1.018, l_max=30, k_max=None):
    """
    Scan all ADE subgroups and compute D_2 for each.

    Parameters
    ----------
    omega_tot : float
        Total density parameter.
    l_max : int
        Maximum multipole.
    k_max : int or None
        Maximum eigenmode index for multiplicity computation.
        If None, determined from CAMB nu_vals (recommended).

    Returns
    -------
    list of dict with keys:
        'name': str
        'order': int
        'ade_type': str
        'D_2': float (muK^2)
        'D_3': float (muK^2)
        'S_2': float (suppression ratio at l=2)
        'first_nonzero': int (first k > 0 with r(k) > 0)
        'n_modes_50': int (number of nonzero r(k) for k <= 50)
    """
    camb_data = _get_camb_transfer(omega_tot, l_max)

    # Set k_max from CAMB if not specified
    if k_max is None:
        k_max = int(camb_data['nu_vals'][-1])

    catalog = build_group_catalog(k_max)

    results = []
    for group in catalog:
        r = group['multiplicities']
        cls = compute_cls_quotient(r, omega_tot, l_max, camb_data)

        # Find first nonzero mode after k=0
        first_nz = None
        for k in range(1, k_max + 1):
            if r[k] > 0:
                first_nz = k
                break

        # Count modes in low-k regime
        n_modes_50 = int(np.sum(r[1:51] > 0))

        sup = compute_suppression(r, camb_data['nu_vals'], camb_data['delta_sq'])

        results.append({
            'name': group['name'],
            'order': group['order'],
            'ade_type': group['ade_type'],
            'D_2': cls.get(2, 0.0),
            'D_3': cls.get(3, 0.0),
            'S_2': sup.get(2, 0.0),
            'first_nonzero': first_nz,
            'n_modes_50': n_modes_50,
        })

    return results


def find_best_topology(target_d2=201.0, omega_tot_range=(1.001, 1.05),
                       n_omega=20, l_max=30, k_max=None):
    """
    Find (Gamma, Omega_tot) that gives D_2 closest to target.

    Scans a grid of Omega_tot values for each ADE group.

    Parameters
    ----------
    target_d2 : float
        Target D_2 in muK^2 (default: Planck 201).
    omega_tot_range : tuple
        Range of Omega_tot to scan.
    n_omega : int
        Number of Omega_tot values to try.
    l_max : int
        Maximum multipole.
    k_max : int or None
        Maximum eigenmode index. If None, determined from first CAMB run.

    Returns
    -------
    list of dict sorted by |D_2 - target|, with keys:
        'name', 'order', 'omega_tot', 'D_2', 'D_3', 'delta_d2'
    """
    omega_values = np.linspace(omega_tot_range[0], omega_tot_range[1], n_omega)

    # Build catalog once (at the largest k_max we'll need)
    # Use first omega_tot to determine k_max from CAMB, then reuse catalog
    catalog = None

    all_results = []

    for omega_tot in omega_values:
        try:
            camb_data = _get_camb_transfer(omega_tot, l_max)
        except Exception:
            continue

        if catalog is None:
            if k_max is None:
                k_max = int(camb_data['nu_vals'][-1])
            catalog = build_group_catalog(k_max)

        for group in catalog:
            r = group['multiplicities']
            cls = compute_cls_quotient(r, omega_tot, l_max, camb_data)
            d2 = cls.get(2, 0.0)
            d3 = cls.get(3, 0.0)

            all_results.append({
                'name': group['name'],
                'order': group['order'],
                'omega_tot': omega_tot,
                'D_2': d2,
                'D_3': d3,
                'delta_d2': abs(d2 - target_d2),
            })

    # Sort by |D_2 - target|
    all_results.sort(key=lambda x: x['delta_d2'])
    return all_results


