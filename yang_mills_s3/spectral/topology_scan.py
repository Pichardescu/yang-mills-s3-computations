def build_group_catalog(k_max=200):
    """
    Build a catalog of all ADE subgroups with their multiplicities.

    Returns
    -------
    list of dict with keys:
        'name': str, human-readable name
        'order': int, |Gamma|
        'ade_type': str, 'cyclic', 'binary_dihedral', 'binary_tetrahedral',
                    'binary_octahedral', 'binary_icosahedral'
        'multiplicities': ndarray of int, shape (k_max+1,)
    """
    catalog = []

    # Cyclic groups
    for n in [2, 3, 4, 5, 6, 7, 8, 10, 12, 15, 20, 24, 30, 60, 120]:
        catalog.append({
            'name': f'Z_{n}',
            'order': n,
            'ade_type': 'cyclic',
            'multiplicities': molien_cyclic(n, k_max),
        })

    # Binary dihedral groups
    for n in [2, 3, 4, 5, 6, 8, 10]:
        catalog.append({
            'name': f'D*_{n}',
            'order': 4 * n,
            'ade_type': 'binary_dihedral',
            'multiplicities': molien_binary_dihedral(n, k_max),
        })

    # Binary tetrahedral
    catalog.append({
        'name': 'T*',
        'order': 24,
        'ade_type': 'binary_tetrahedral',
        'multiplicities': molien_binary_tetrahedral(k_max),
    })

    # Binary octahedral
    catalog.append({
        'name': 'O*',
        'order': 48,
        'ade_type': 'binary_octahedral',
        'multiplicities': molien_binary_octahedral(k_max),
    })

    # Binary icosahedral
    catalog.append({
        'name': 'I*',
        'order': 120,
        'ade_type': 'binary_icosahedral',
        'multiplicities': molien_binary_icosahedral(k_max),
    })

    return catalog


