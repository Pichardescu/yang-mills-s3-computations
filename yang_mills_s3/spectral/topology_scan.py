def print_scan_results(results, target_d2=201.0):
    """
    Print scan results as a formatted table.

    Parameters
    ----------
    results : list of dict from scan_all_ade_groups
    target_d2 : float
    """
    # Sort by |D_2 - target|
    sorted_results = sorted(results, key=lambda x: abs(x['D_2'] - target_d2))

    header = f"{'Group':<12} {'|G|':>5} {'Type':<20} {'D_2 (uK^2)':>12} {'|D_2-201|':>10} {'S_2':>8} {'1st k>0':>8} {'modes<50':>9}"
    print(header)
    print("-" * len(header))

    for r in sorted_results:
        first_nz = r['first_nonzero'] if r['first_nonzero'] is not None else '-'
        print(f"{r['name']:<12} {r['order']:>5} {r['ade_type']:<20} "
              f"{r['D_2']:>12.2f} {abs(r['D_2'] - target_d2):>10.2f} "
              f"{r['S_2']:>8.4f} {str(first_nz):>8} {r['n_modes_50']:>9}")
