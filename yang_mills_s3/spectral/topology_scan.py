"""
Topology scan: C_l for all finite quotients S^3/Gamma.

Scans the ADE classification of finite subgroups Gamma < SU(2) to find
which topology S^3/Gamma best matches the Planck CMB quadrupole D_2 = 201 muK^2.

The finite subgroups of SU(2) (ADE classification):
  - Cyclic:             Z_n  (order n),  for n >= 1
  - Binary dihedral:    D*_n (order 4n), for n >= 2
  - Binary tetrahedral: T*   (order 24)
  - Binary octahedral:  O*   (order 48)
  - Binary icosahedral: I*   (order 120)

For each Gamma, the multiplicity of Gamma-invariant vectors in the
(k+1)-dimensional irrep V_{k/2} of SU(2) is:

    r_Gamma(k) = (1/|Gamma|) * sum_{g in Gamma} chi_{k/2}(g)

where chi_j(theta) = sin((2j+1)*theta/2) / sin(theta/2) is the SU(2)
character at rotation angle theta.

The total number of Gamma-invariant scalar harmonics at level k is
r_Gamma(k) * (k+1), where the factor (k+1) comes from the SU(2)_R
representation (left Clifford quotient preserves SU(2)_R).

The CMB angular power spectrum on S^3/Gamma is computed via the
suppression ratio:

    S_l = C_l^{Gamma} / C_l^{S^3}
        = [sum_nu T_l(nu)^2 * r_Gamma(nu-1) * nu] / [sum_nu T_l(nu)^2 * nu^2]

where T_l(nu) is the CAMB Boltzmann transfer function (topology-independent),
nu = k+1 is the eigenmode index, and the weight r_Gamma(k) * nu counts
the total Gamma-invariant scalar harmonics.

NUMERICAL: Topology scan across all ADE subgroups of SU(2).

References:
    - Cornish, Spergel, Starkman, CQG 15, 2657 (1998)
    - Aurich, Lustig, Steiner, CQG 22, 2061 (2005)
    - Luminet et al., Nature 425, 593 (2003)
    - COMPACT Collaboration, A&A 683, A62 (2024)
"""

import numpy as np

try:
    import camb
    HAS_CAMB = True
except ImportError:
    HAS_CAMB = False


# ===================================================================
# SU(2) character
# ===================================================================

def character_su2(k, theta):
    """
    Character of V_{k/2} (dimension k+1) at rotation angle theta.

    chi_{k/2}(theta) = sin((k+1)*theta/2) / sin(theta/2)

    Special cases handled via L'Hopital:
      theta = 0:    chi = k + 1
      theta = 2*pi: chi = (-1)^k * (k + 1)

    Parameters
    ----------
    k : int
        Level index (non-negative). Representation has dimension k+1.
    theta : float
        Rotation angle in [0, 2*pi].

    Returns
    -------
    float : character value
    """
    half = theta / 2.0
    sin_denom = np.sin(half)

    if abs(sin_denom) < 1e-14:
        # L'Hopital: lim sin((k+1)x)/sin(x) = (k+1)*cos((k+1)x)/cos(x)
        cos_num = np.cos((k + 1) * half)
        cos_denom = np.cos(half)
        if abs(cos_denom) < 1e-14:
            # theta = pi: use exact formula
            return np.sin((k + 1) * np.pi / 2) / np.sin(np.pi / 2)
        return (k + 1) * cos_num / cos_denom

    return np.sin((k + 1) * half) / sin_denom


